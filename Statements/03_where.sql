-- Filtra todos los datos de la tabla "users" con edad igual a 21
SELECT * FROM users WHERE age = 21;
-- Filtra todos los nombres de la tabla "users" con edad igual a 21
SELECT name  FROM users WHERE age = 21;
-- Filtra todos los nombres de la tabla "users" distintos con edad igual a 21
SELECT DISTINCT name FROM users WHERE age = 21;
